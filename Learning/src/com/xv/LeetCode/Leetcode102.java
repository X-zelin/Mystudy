package com.xv.LeetCode;

public class Leetcode102 {
    class TreeNode{
        int val;
        TreeNode left;
        TreeNode right;
        public TreeNode(int val){
            this.val = val;
        }
        public TreeNode(TreeNode left,int val,TreeNode right){
            this.val = val;
            this.left = left;
            this.right = right;
        }
    }


}
